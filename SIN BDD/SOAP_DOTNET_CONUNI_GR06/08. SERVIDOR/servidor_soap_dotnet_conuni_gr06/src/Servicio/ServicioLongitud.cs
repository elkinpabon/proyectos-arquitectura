namespace Ec.Edu.Monster.Servicio;

public class ServicioLongitud
{
    public double MetrosAPies(double metros) => metros * 3.28084;

    public double KilometrosAMillas(double kilometros) => kilometros * 0.621371;

    public double CentimetrosAPulgadas(double centimetros) => centimetros / 2.54;

    public double YardasAMetros(double yardas) => yardas * 0.9144;

    public double MilimetrosAPulgadas(double milimetros) => milimetros / 25.4;
}
